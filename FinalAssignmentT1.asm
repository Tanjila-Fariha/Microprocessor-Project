.MODEL SMALL
 
ORG 100H

.DATA

; declare variables here 
ARR1 DB 13 DUP(?)   
COUNT DB 0
STR1 DB "ENTER A NUMBER (2 DIGITS):$"  
J DW 0

.CODE
MAIN PROC

; initialize DS

MOV AX,@DATA
MOV DS,AX
 
; enter your code here
LEA DX,STR1
MOV AH,9
INT 21H
MOV AH,1
INT 21H 
SUB AL,030H
MOV BH,AL 
MOV AH,1
INT 21H
SUB AL,030H
MOV BL,AL
MOV AL,10
MUL BH
MOV BH,0
ADD AX,BX 
MOV BX,AX  
CALL M1 
MOV AH,2
MOV DL,0DH
INT 21H
MOV DL,0AH
INT 21H
MOV CL,COUNT
MOV CH,0 
MOV SI,0
LOOP2:
MOV AL,ARR1[SI]
MOV AH,0
MOV DL,10
DIV DL
MOV DX,AX
ADD DL,030H
MOV AH,2
INT 21H 
MOV DL,DH
ADD DL,030H
MOV AH,2
INT 21H
INC SI 
MOV AH,2
MOV DL,0DH
INT 21H
MOV DL,0AH
INT 21H
LOOP LOOP2  
RET
M1 PROC
    CMP BL,0
    JE ZERO
    MOV SI,0
    MOV ARR1[SI],0 
    INC COUNT
    INC SI
    MOV ARR1[SI],1 
    INC COUNT
    INC SI
    LOOP1:
    MOV J,SI
    DEC SI
    MOV CL,ARR1[SI]
    DEC SI
    MOV DL,ARR1[SI]
    ADD CL,DL
    MOV CH,0
    MOV BH,0
    CMP CX,BX
    JG E
    MOV SI,J
    MOV ARR1[SI],CL  
    INC COUNT
    INC SI
    JMP LOOP1
    ZERO:
    MOV SI,0
    MOV ARR1[SI],0
    INC COUNT 
    E:
    RET
    M1 ENDP
END


